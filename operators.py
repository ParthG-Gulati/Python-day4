x = 10
y = 6

# arithmetic operators

print("x + y =", x + y)
print("x - y =", x - y)
print("x * y =", x * y)
print("x / y =", x / y)
print("x // y =", x // y)
print("x ** y =", x ** y)

# comparison operators

print("x > y is", x > y)
print("x < y is", x < y)
print("x == y is", x == y)
print("x != y is", x != y)
print("x >= y is", x >= y)
print("x <= y is", x <= y)
